<!-- =======================for-loop=================================================== -->
<?php
for($i=0; $i<=10; $i++)
{
    if($i>=5)
    echo $i ."<br>";
}

?>
<!-- =======================for-loop=================================================== -->

<?php
for($i=0; $i<=10; $i++):

    if($i>=5):
    echo $i ."<br>";
    endif; 
endfor;
?>
<!-- =======================for-loop=================================================== -->
<?php
for($i=1; $i<=10; $i++):
     
    // echo $i."-";
    if($i==10){
        echo $i;
    } 
    else{
        echo $i."-";
    }  
    
endfor;
?>

<!-- =======================factorial program=================================================== -->
<?php
$fact=1;
for($i=1; $i<=5; $i++):
    $fact=$fact*$i; 
endfor;
echo  "<br>";
echo $fact; 
?>

